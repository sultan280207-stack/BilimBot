# ⚠️ /detect КОМАНДАСЫ — Скам және вирус анықтауы
# =============================================================================
async def detect(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """
    Берілген мәтін немесе сілтемені Groq AI арқылы талдайды.
    Скам, фишинг, вирус белгілерін анықтап, қауіп деңгейін береді.
    """
    # context.args — командадан кейінгі аргументтер тізімі
    text = " ".join(context.args)

    # Аргумент жоқ болса — пайдалану нұсқауын көрсету
    if not text:
        await update.message.reply_text(
            "Қолдану: /detect [мәтін немесе сілтеме]\n\n"
            "Мысалдар:\n"
            "• /detect Сіз 1 000 000 тг ұтып алдыңыз! Картаңызды жіберіңіз\n"
            "• /detect http://suspicious-site.ru/win-prize\n"
            "• /detect Банктен: аккаунтыңыз бұғатталды, осы сілтемеге кіріңіз"
        )
        return

    await update.message.reply_text("🔍 Тексерілуде...")

    # -----------------------------------------------------------------
    # AI-ға берілетін нұсқаулар (system prompt)
    # Мәтінді 3 категория бойынша талдауды сұраймыз:
    #   1. Скам белгілері
    #   2. Вирус/зиянды сілтеме белгілері
    #   3. Әлеуметтік инженерия
    # -----------------------------------------------------------------
    system_prompt = (
        "Сен киберқауіпсіздік маманысың. Берілген мәтінді немесе сілтемені мұқият талдап, "
        "скам (алаяқтық) және вирус/зиянды бағдарлама белгілерін тексер.\n\n"
        "Міндетті түрде мынаны тексер:\n"
        "1. СКАМ белгілері: жалған ұтыс, тез байыту схемалары, банк/мемлекет атынан алдау, "
        "жеке деректерді сұрау, қорқыту, шұғыл әрекет талап ету\n"
        "2. ВИРУС/ЗИЯНДЫ СІЛТЕМЕ белгілері: күдікті домен аттары (typosquatting), "
        "HTTP (HTTPS емес), қысқартылған сілтемелер, фишинг URL-дері, "
        "белгісіз файл кеңейтімдері (.exe, .bat, .vbs), зиянды скрипт белгілері\n"
        "3. ӘЛЕУМЕТТІК ИНЖЕНЕРИЯ: психологиялық қысым, жалған шұғылдық, "
        "беделді ұйым атынан жазу\n\n"
        "Жауапты ҚАЗАҚША осы форматта бер:\n"
        "━━━━━━━━━━━━━━━\n"
        "🔴/🟡/🟢 ҚАУІП ДЕҢГЕЙІ: [ЖОҒАРЫ / ОРТА / ТӨМЕН]\n"
        "📂 КАТЕГОРИЯ: [Скам / Фишинг / Вирус сілтемесі / Зиянды файл / Таза]\n"
        "━━━━━━━━━━━━━━━\n"
        "🔎 АНЫҚТАЛҒАН БЕЛГІЛЕР:\n"
        "• [белгі 1]\n"
        "• [белгі 2]\n"
        "━━━━━━━━━━━━━━━\n"
        "💡 ҚОРЫТЫНДЫ: [1-2 сөйлем түсіндірме]\n"
        "🛡️ ҰСЫНЫС: [пайдаланушыға нақты кеңес]\n\n"
        "Егер мәтін/сілтеме қауіпсіз болса, 🟢 деп белгіле және неге қауіпсіз екенін түсіндір."
    )

    # Groq API-не сұраныс жіберу — LLaMA 3.3 70B моделі қолданылады
    res = client.chat.completions.create(
        model="llama-3.3-70b-versatile",
        messages=[
            {"role": "system", "content": system_prompt},
            {"role": "user",   "content": f"Тексер:\n{text}"}
        ],
        max_tokens=600  # Жауаптың максималды ұзындығы
    )

    # AI жауабын пайдаланушыға жіберу
    result = res.choices[0].message.content
    await update.message.reply_text(result)



